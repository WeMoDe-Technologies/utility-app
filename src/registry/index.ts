export const UTILITY_MAP = Object.fromEntries(
  UTILITY_REGISTRY.map((u) => [u.id, u])
) as Record<string, UtilityDefinition>;

export function getUtilityById(id: string): UtilityDefinition | undefined {
  return UTILITY_MAP[id];
}

export function searchUtilities(query: string): UtilityDefinition[] {
  if (!query.trim()) return UTILITY_REGISTRY;
  const q = query.toLowerCase();
  return UTILITY_REGISTRY.filter(
    (u) =>
      u.title.toLowerCase().includes(q) ||
      u.description.toLowerCase().includes(q) ||
      u.category.toLowerCase().includes(q)
  );
}
